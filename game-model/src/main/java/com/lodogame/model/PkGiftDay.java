package com.lodogame.model;

public class PkGiftDay {
	private int rank;
	private int giftYb;
	private int giftScore;
	private String gift;
	
	

	public String getGift() {
		return gift;
	}

	public void setGift(String gift) {
		this.gift = gift;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

	public int getGiftYb() {
		return giftYb;
	}

	public void setGiftYb(int giftYb) {
		this.giftYb = giftYb;
	}

	public int getGiftScore() {
		return giftScore;
	}

	public void setGiftScore(int giftScore) {
		this.giftScore = giftScore;
	}

}
