package com.lodogame.model;

public class SystemFloorForces {
	private int floor;
	private int forcesId;
	private int minRand;
	private int maxRand;
	private int isBoss;

	public int getFloor() {
		return floor;
	}

	public void setFloor(int floor) {
		this.floor = floor;
	}

	public int getForcesId() {
		return forcesId;
	}

	public void setForcesId(int forcesId) {
		this.forcesId = forcesId;
	}

	public int getMinRand() {
		return minRand;
	}

	public void setMinRand(int minRand) {
		this.minRand = minRand;
	}

	public int getMaxRand() {
		return maxRand;
	}

	public void setMaxRand(int maxRand) {
		this.maxRand = maxRand;
	}

	public int getIsBoss() {
		return isBoss;
	}

	public void setIsBoss(int isBoss) {
		this.isBoss = isBoss;
	}

}
