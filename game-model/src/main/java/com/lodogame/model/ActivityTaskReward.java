package com.lodogame.model;

public class ActivityTaskReward {

	private int activityTaskRewardId;

	private int point;

	private int toolType;

	private int toolId;

	private int toolNum;

	public int getActivityTaskRewardId() {
		return activityTaskRewardId;
	}

	public void setActivityTaskRewardId(int activityTaskRewardId) {
		this.activityTaskRewardId = activityTaskRewardId;
	}

	public int getPoint() {
		return point;
	}

	public void setPoint(int point) {
		this.point = point;
	}

	public int getToolType() {
		return toolType;
	}

	public void setToolType(int toolType) {
		this.toolType = toolType;
	}

	public int getToolId() {
		return toolId;
	}

	public void setToolId(int toolId) {
		this.toolId = toolId;
	}

	public int getToolNum() {
		return toolNum;
	}

	public void setToolNum(int toolNum) {
		this.toolNum = toolNum;
	}

}
