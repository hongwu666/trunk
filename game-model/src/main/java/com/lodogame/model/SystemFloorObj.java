package com.lodogame.model;

public class SystemFloorObj {
	private int floor;
	private int objId;
	private int minRand;
	private int maxRand;
	private int isBoss;
	private int objType;

	public int getFloor() {
		return floor;
	}

	public void setFloor(int floor) {
		this.floor = floor;
	}

	public int getMinRand() {
		return minRand;
	}

	public void setMinRand(int minRand) {
		this.minRand = minRand;
	}

	public int getMaxRand() {
		return maxRand;
	}

	public void setMaxRand(int maxRand) {
		this.maxRand = maxRand;
	}

	public int getIsBoss() {
		return isBoss;
	}

	public void setIsBoss(int isBoss) {
		this.isBoss = isBoss;
	}

	public int getObjId() {
		return objId;
	}

	public void setObjId(int objId) {
		this.objId = objId;
	}

	public int getObjType() {
		return objType;
	}

	public void setObjType(int objType) {
		this.objType = objType;
	}

}
