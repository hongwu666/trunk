package com.lodogame.model;

public class SystemArenaGift {
	private int arenaGiftId;
	private int groupId;
	private int lowerRank;
	private int upperRank;
	private int giftId;
	public int getArenaGiftId() {
		return arenaGiftId;
	}
	public void setArenaGiftId(int arenaGiftId) {
		this.arenaGiftId = arenaGiftId;
	}
	public int getGroupId() {
		return groupId;
	}
	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}
	public int getLowerRank() {
		return lowerRank;
	}
	public void setLowerRank(int lowerRank) {
		this.lowerRank = lowerRank;
	}
	public int getUpperRank() {
		return upperRank;
	}
	public void setUpperRank(int upperRank) {
		this.upperRank = upperRank;
	}
	public int getGiftId() {
		return giftId;
	}
	public void setGiftId(int giftId) {
		this.giftId = giftId;
	}
	
	
}
