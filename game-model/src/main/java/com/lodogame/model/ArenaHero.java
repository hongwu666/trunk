package com.lodogame.model;

public class ArenaHero {

	/**
	 * 最大血量
	 */
	private long maxLife;

	/**
	 * 当前血量
	 */
	private long life;

	public long getMaxLife() {
		return maxLife;
	}

	public void setMaxLife(long maxLife) {
		this.maxLife = maxLife;
	}

	public long getLife() {
		return life;
	}

	public void setLife(long life) {
		this.life = life;
	}

}
