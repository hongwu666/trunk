package com.lodogame.model;

public class UserStone {

	private String userStoneId;
	private String userId;
	private int stoneId;
	private int stoneNum;

	public String getUserStoneId() {
		return userStoneId;
	}

	public void setUserStoneId(String userStoneId) {
		this.userStoneId = userStoneId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public int getStoneId() {
		return stoneId;
	}

	public void setStoneId(int stoneId) {
		this.stoneId = stoneId;
	}

	public int getStoneNum() {
		return stoneNum;
	}

	public void setStoneNum(int stoneNum) {
		this.stoneNum = stoneNum;
	}

}
