package com.lodogame.model;

public class SystemDeifyNode {

	private int heroId;
	
	private int DeifyNodeLevel;
	
	private int physicalAttack;
	
	private int physicalDefense;
	
	private int life;
	
	private int needGodSoulNum;

	public int getHeroId() {
		return heroId;
	}

	public void setHeroId(int heroId) {
		this.heroId = heroId;
	}

	public int getDeifyNodeLevel() {
		return DeifyNodeLevel;
	}

	public void setDeifyNodeLevel(int deifyNodeLevel) {
		DeifyNodeLevel = deifyNodeLevel;
	}

	public int getPhysicalAttack() {
		return physicalAttack;
	}

	public void setPhysicalAttack(int physicalAttack) {
		this.physicalAttack = physicalAttack;
	}

	public int getPhysicalDefense() {
		return physicalDefense;
	}

	public void setPhysicalDefense(int physicalDefense) {
		this.physicalDefense = physicalDefense;
	}

	public int getLife() {
		return life;
	}

	public void setLife(int life) {
		this.life = life;
	}

	public int getNeedGodSoulNum() {
		return needGodSoulNum;
	}

	public void setNeedGodSoulNum(int needGodSoulNum) {
		this.needGodSoulNum = needGodSoulNum;
	}
    
}
